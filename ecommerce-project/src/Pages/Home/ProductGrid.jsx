
import { Product } from "./Product";
export function ProductGrid({ products, cartData }) {
    
   return (
    <div className="products-grid">
      {products.map((product) => {

        return (
          <>
            <Product key ={product.id} product={product} cartData={cartData}/>
          </>
        );
      })}
    </div>
  );
}
